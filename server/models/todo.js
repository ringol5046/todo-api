var mongoose = require('mongoose');

var Todo = mongoose.model('Todo', {
  title:{
    type:String,
    required:true,
    trim:true
  },
  text: {
    type: String,
    required: true,
    minlength: 1,
    trim: true
  },
  completed: {
    type: Boolean,
    default: false
  },
  completedAt: {
    type: Number,
    default: null
  },
  _creator: {
    type: mongoose.Schema.Types.ObjectId,
    required: true
  }
});

module.exports = { Todo };
